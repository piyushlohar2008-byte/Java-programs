public class forEachLoop {
    public static void main(String[] args) {

        String names[] = {"Piyush", "Aditya", "Raj", "Aryan"};

        for(String name:names){
            System.out.println("Name is : "+name);
        }
        for(String N:names){
            System.out.println("Name is : "+N);
        }
    }
}